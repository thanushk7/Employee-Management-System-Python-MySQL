class Employee:

    def __init__(
        self,
        emp_id,
        name,
        age,
        gender,
        department,
        designation,
        salary,
        email,
        phone,
        joining_date
    ):

        self.emp_id = emp_id
        self.name = name
        self.age = age
        self.gender = gender
        self.department = department
        self.designation = designation
        self.salary = salary
        self.email = email
        self.phone = phone
        self.joining_date = joining_date